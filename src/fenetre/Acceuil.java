    private void btnSoloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSoloActionPerformed
        // nouvelle partie
    int n=0;//niveau    
    JOptionPane jop = new JOptionPane(), jop2 = new JOptionPane();
    String nom = jop.showInputDialog(null, "Veuillez entrer votre pseudo !", "Bonjour...?", JOptionPane.QUESTION_MESSAGE);
     String[] difficulte = {"Facile", "Moyen", "Difficile"};
    String diff=(String)jop2.showInputDialog(null, "Bienvenue " + nom, "Choissez un niveau",JOptionPane.QUESTION_MESSAGE,null,difficulte,difficulte[2]);
      switch(diff){
          case "Facile": n=4;break;
          case "Moyen": n=5;break;
          case "Difficile": n=6;break;    
      }  
      
      code.Partie p=new Partie(n,new Joueur(nom),1);
      switch(n){
          case 4: new PlateauJeu(p).setVisible(true);this.dispose();break;
          case 5: new PlateauJeuLvl2(p).setVisible(true);this.dispose();break;
          case 6: new PlateauJeuLvl3(p).setVisible(true);this.dispose();break;    
      }
    }//GEN-LAST:event_btnSoloActionPerformed

